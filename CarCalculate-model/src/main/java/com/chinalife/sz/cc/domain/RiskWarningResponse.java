package com.chinalife.sz.cc.domain;

import java.io.Serializable;

/**
 * Created by tianwei on 2016/7/20.
 * 风险警示信息
 */
public class RiskWarningResponse implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -4343440993549454347L;
	private String demandNo;
	private String serialNo;
	private String riskWarningType;
	private String claimSequenceNo;
	private String insurerCode;
	private String lossTime;
	private String lossArea;

	@Override
	public String toString() {
		return "RiskWarningResponse{" +
				"demandNo='" + demandNo + '\'' +
				", serialNo='" + serialNo + '\'' +
				", riskWarningType='" + riskWarningType + '\'' +
				", claimSequenceNo='" + claimSequenceNo + '\'' +
				", insurerCode='" + insurerCode + '\'' +
				", lossTime='" + lossTime + '\'' +
				", lossArea='" + lossArea + '\'' +
				'}';
	}

	public String getDemandNo() {
		return demandNo;
	}

	public void setDemandNo(String demandNo) {
		this.demandNo = demandNo;
	}

	public String getSerialNo() {
		return serialNo;
	}

	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	public String getRiskWarningType() {
		return riskWarningType;
	}

	public void setRiskWarningType(String riskWarningType) {
		this.riskWarningType = riskWarningType;
	}

	public String getClaimSequenceNo() {
		return claimSequenceNo;
	}

	public void setClaimSequenceNo(String claimSequenceNo) {
		this.claimSequenceNo = claimSequenceNo;
	}

	public String getInsurerCode() {
		return insurerCode;
	}

	public void setInsurerCode(String insurerCode) {
		this.insurerCode = insurerCode;
	}

	public String getLossTime() {
		return lossTime;
	}

	public void setLossTime(String lossTime) {
		this.lossTime = lossTime;
	}

	public String getLossArea() {
		return lossArea;
	}

	public void setLossArea(String lossArea) {
		this.lossArea = lossArea;
	}
}
