package com.chinalife.sz.cc.model.combos;

import org.apache.ibatis.type.Alias;

import java.io.Serializable;

/**
 * Created by tianwei on 2016/12/23.
 */
@Alias("CombosSchemaDTO")
public class CombosSchemaDTO implements Serializable {
    private static final long serialVersionUID = 2100808019439153697L;

    private Long id;
    private String combosName;
    private String combosInfo;
    private String itemKindList;
    private String inputDate;
    private String handlerCode;
    private String handlerName;

    public String toString() {
        return "CombosSchemaDTO{" +
                "id=" + id +
                ", combosName='" + combosName + '\'' +
                ", combosInfo='" + combosInfo + '\'' +
                ", itemKindList='" + itemKindList + '\'' +
                ", inputDate='" + inputDate + '\'' +
                ", handlerCode='" + handlerCode + '\'' +
                ", handlerName='" + handlerName + '\'' +
                '}';
    }


    public String getHandlerCode() {
        return handlerCode;
    }

    public void setHandlerCode(String handlerCode) {
        this.handlerCode = handlerCode;
    }

    public String getHandlerName() {
        return handlerName;
    }

    public void setHandlerName(String handlerName) {
        this.handlerName = handlerName;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCombosName() {
        return combosName;
    }

    public void setCombosName(String combosName) {
        this.combosName = combosName;
    }

    public String getCombosInfo() {
        return combosInfo;
    }

    public void setCombosInfo(String combosInfo) {
        this.combosInfo = combosInfo;
    }

    public String getItemKindList() {
        return itemKindList;
    }

    public void setItemKindList(String itemKindList) {
        this.itemKindList = itemKindList;
    }

    public String getInputDate() {
        return inputDate;
    }

    public void setInputDate(String inputDate) {
        this.inputDate = inputDate;
    }
}
