package com.chinalife.sz.cc.domain;

import java.io.Serializable;

/**
 * Created by tianwei on 2016/9/6.
 */
public class ResponseMessage implements Serializable {

    private static final long serialVersionUID = -782239487061986203L;
    private String errorNo;
    private String errorType;
    private String resultCode;
    private String resultInfoDesc;

    @Override
    public String toString() {
        return "ResponseMessage{" +
                "errorNo='" + errorNo + '\'' +
                ", errorType='" + errorType + '\'' +
                ", resultCode='" + resultCode + '\'' +
                ", resultInfoDesc='" + resultInfoDesc + '\'' +
                '}';
    }

    public String getErrorNo() {
        return errorNo;
    }

    public void setErrorNo(String errorNo) {
        this.errorNo = errorNo;
    }

    public String getErrorType() {
        return errorType;
    }

    public void setErrorType(String errorType) {
        this.errorType = errorType;
    }

    public String getResultCode() {
        return resultCode;
    }

    public void setResultCode(String resultCode) {
        this.resultCode = resultCode;
    }

    public String getResultInfoDesc() {
        return resultInfoDesc;
    }

    public void setResultInfoDesc(String resultInfoDesc) {
        this.resultInfoDesc = resultInfoDesc;
    }
}
