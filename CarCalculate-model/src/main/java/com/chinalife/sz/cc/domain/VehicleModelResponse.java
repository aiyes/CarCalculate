package com.chinalife.sz.cc.domain;

import java.io.Serializable;
import java.util.Arrays;

/**
 * Created by tianwei on 2016/7/20.
 */
public class VehicleModelResponse implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1991308971802746228L;
	private String RequestType;
	private String carBlackFlag;
	private String carGrayFlag;
	private TMBVehicleInfoResponse TMBVehicleInfoEhm;
	private VehicleModelDataResponse[] VehicleModelDataArr;
    private TransResultEhm transResultEhm;


	@Override
	public String toString() {
		return "VehicleModelResponse{" +
				"RequestType='" + RequestType + '\'' +
				", carBlackFlag='" + carBlackFlag + '\'' +
				", carGrayFlag='" + carGrayFlag + '\'' +
				", TMBVehicleInfoEhm=" + TMBVehicleInfoEhm +
				", VehicleModelDataArr=" + Arrays.toString(VehicleModelDataArr) +
				", transResultEhm=" + transResultEhm +
				'}';
	}

	public String getRequestType() {
		return RequestType;
	}



	public void setRequestType(String requestType) {
		RequestType = requestType;
	}

	public String getCarBlackFlag() {
		return carBlackFlag;
	}

	public void setCarBlackFlag(String carBlackFlag) {
		this.carBlackFlag = carBlackFlag;
	}

	public String getCarGrayFlag() {
		return carGrayFlag;
	}

	public void setCarGrayFlag(String carGrayFlag) {
		this.carGrayFlag = carGrayFlag;
	}

	public TMBVehicleInfoResponse getTMBVehicleInfoEhm() {
		return TMBVehicleInfoEhm;
	}

	public void setTMBVehicleInfoEhm(TMBVehicleInfoResponse TMBVehicleInfoEhm) {
		this.TMBVehicleInfoEhm = TMBVehicleInfoEhm;
	}

	public VehicleModelDataResponse[] getVehicleModelDataArr() {
		return VehicleModelDataArr;
	}

	public void setVehicleModelDataArr(VehicleModelDataResponse[] vehicleModelDataArr) {
		VehicleModelDataArr = vehicleModelDataArr;
	}

	public TransResultEhm getTransResultEhm() {
		return transResultEhm;
	}

	public void setTransResultEhm(TransResultEhm transResultEhm) {
		this.transResultEhm = transResultEhm;
	}
}
